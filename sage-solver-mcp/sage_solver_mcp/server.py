# Tools that involve solving and can benefit from retry + fallback
_SOLVE_TOOLS = {"solve_optimization", "solve_from_file", "check_feasibility"}

_TOOL_HANDLERS: dict[str, Any] = {}  # populated after handler definitions


@server.call_tool()
async def call_tool(
    name: str, arguments: dict[str, Any]
) -> list[types.TextContent]:
    handler = _TOOL_HANDLERS.get(name)
    if handler is None:
        return _error_text(f"Unknown tool: {name!r}")

    # First attempt
    try:
        return await handler(arguments)
    except (ValidationError, ValueError) as exc:
        # User input errors — no retry needed
        return _error_text(f"Validation error in {name}: {exc}")
    except (FileNotFoundError, OSError) as exc:
        # File errors — no retry needed
        return _error_text(
            f"File not found or not readable: {exc}\n\n"
            "Suggestions:\n"
            "- Check that the file path is correct.\n"
            "- Use an absolute path or ~ for your home directory.\n"
            "- Ensure the file exists and you have read permission."
        )
    except Exception as first_error:
        logger.warning("Technical error in tool %r (attempt 1): %s", name, first_error)

        # Only retry + fallback for solve tools
        if name not in _SOLVE_TOOLS:
            return _tech_error_text(name, str(first_error))

        # Step 1: Retry once
        last_error = first_error
        try:
            logger.info("Retrying tool %r...", name)
            return await handler(arguments)
        except Exception as retry_error:
            last_error = retry_error
            logger.warning("Technical error in tool %r (attempt 2): %s", name, retry_error)

        # Step 2: Fallback to scipy/PuLP
        try:
            return await _fallback_solve_bridge(name, arguments, last_error)
        except Exception as fallback_error:
            logger.error("Fallback also failed for tool %r: %s", name, fallback_error)

        # Step 3: Graceful failure
        return _all_failed_text(name, str(last_error))


# ---------------------------------------------------------------------------
# Fallback solver bridge
# ---------------------------------------------------------------------------


async def _fallback_solve_bridge(
    tool_name: str, args: dict[str, Any], original_error: Exception
) -> list[types.TextContent]:
    """Attempt to solve using scipy/PuLP when the primary solver fails."""
    try:
        model, si = _parse_model(args)
    except Exception:
        raise RuntimeError("Cannot parse model for fallback") from original_error

    result, solver_name = fallback_solve(si)

    _state.last_result = result
    _state.last_model = model
    _state.last_solver_input = si
    _state.last_iis = None  # fallback solvers don't compute IIS

    summary = _format_solution_summary(result, model)
    return _fallback_text(tool_name, solver_name, summary)


