def _success_text(tool_name: str, content: str) -> list[types.TextContent]:
    """Response for optimal / feasible results."""
    header = f"[Sage MCP] \u2713 Solved using sage:{tool_name}"
    return [types.TextContent(type="text", text=f"{header}\n\n{content}")]


def _info_text(tool_name: str, content: str) -> list[types.TextContent]:
    """Response for infeasible / unbounded — valid results, NOT errors."""
    header = (
        f"[Sage MCP] \u2713 sage:{tool_name} completed \u2014 "
        "problem is infeasible/unbounded.\n"
        "This is a valid result, not an error. See details below."
    )
    return [types.TextContent(type="text", text=f"{header}\n\n{content}")]


def _tech_error_text(tool_name: str, reason: str) -> list[types.TextContent]:
    """Response for technical errors (exception, crash, timeout)."""
    header = f"[Sage MCP] \u2717 sage:{tool_name} encountered a technical error \u2014 {reason}"
    return [types.TextContent(type="text", text=header)]


def _fallback_text(tool_name: str, solver_name: str, content: str) -> list[types.TextContent]:
    """Response when a fallback solver produced the result."""
    header = (
        f"[Sage MCP] Sage unavailable. Falling back to {solver_name} solver...\n"
        f"Note: This is a fallback result from {solver_name}, not from SAGE's primary solver."
    )
    return [types.TextContent(type="text", text=f"{header}\n\n{content}")]


def _all_failed_text(tool_name: str, last_error: str) -> list[types.TextContent]:
    """Response when all solvers (primary + fallback) failed."""
    header = (
        f"[Sage MCP] \u2717 All solvers failed due to technical errors.\n"
        f"Last error: {last_error}\n"
        "Suggestions: check your inputs, variable bounds, and server logs."
    )
    return [types.TextContent(type="text", text=header)]


def _text(content: str) -> list[types.TextContent]:
    """Generic text response (for non-solve tools like read_data, templates)."""
    return [types.TextContent(type="text", text=f"[Sage MCP]\n\n{content}")]


def _error_text(message: str) -> list[types.TextContent]:
    """Validation / user-facing error (not a technical error)."""
    return [types.TextContent(type="text", text=f"[Sage MCP] [SAGE Error]\n{message}")]


def _detect_model_type(data: dict[str, Any]) -> str:
    """Infer the model type from a JSON payload.

    Checks an explicit ``problem_type`` field first, then falls back to
    structural detection based on key presence.
    """
    explicit = data.get("problem_type", "").lower()
    if explicit in ("lp", "mip", "portfolio", "scheduling"):
        return explicit

    if "assets" in data and "covariance_matrix" in data:
        return "portfolio"
    if "workers" in data and "shifts" in data:
        return "scheduling"
    if "variables" in data and "constraints" in data:
        # Distinguish MIP vs LP by var_type field presence.
        # variables may still be a JSON string at this point (parsed later by model_validator).
        vars_ = data.get("variables", [])
        if isinstance(vars_, str):
            try:
                vars_ = json.loads(vars_)
            except (ValueError, TypeError):
                vars_ = []
        if vars_ and any(
            isinstance(v, dict) and v.get("var_type", "continuous") != "continuous"
            for v in vars_
        ):
            return "mip"
        return "lp"

    return "lp"


def _parse_model(data: dict[str, Any]) -> tuple[AnyModel, SolverInput]:
    """Parse a JSON dict into a typed model and build the SolverInput."""
    model_type = _detect_model_type(data)
    # Remove meta-field before passing to Pydantic
    data = {k: v for k, v in data.items() if k != "problem_type"}

    if model_type == "portfolio":
        model = PortfolioModel.model_validate(data)
        si = build_from_portfolio(model)
    elif model_type == "scheduling":
        model = SchedulingModel.model_validate(data)
        si = build_from_scheduling(model)
    elif model_type == "mip":
        model = MIPModel.model_validate(data)
        si = build_from_mip(model)
    else:
        model = LPModel.model_validate(data)
        si = build_from_lp(model)

    return model, si


def _format_solution_summary(result: SolverResult, model: AnyModel) -> str:
    """Return a concise solution summary with explanation."""
    explanation = explain_result(result, model, "standard")
    lines = [explanation]

    if result.status == "optimal" and result.variable_values:
        lines.append("\nVariable values:")
        for name, val in sorted(result.variable_values.items()):
            lines.append(f"  {name}: {val:.6g}")

    if result.status == "infeasible" and result.iis:
        lines.append(f"\nConflicting constraints: {', '.join(result.iis.conflicting_constraints)}")

    return "\n".join(lines)


def _classify_model(model: AnyModel) -> str:
    """Classify a model into a tier using sage-solver-core classifier.

    Returns:
        Tier string: 'instant', 'fast', or 'background'.
    """
    try:
        from sage_solver_core.classifier import classify
        classification = classify(model)
        return classification.tier
    except Exception:
        logger.debug("Classifier failed, defaulting to instant", exc_info=True)
        return "instant"


def _solve_inline(
    si: SolverInput, model: AnyModel, tier: str
) -> SolverResult:
    """Solve a model inline (synchronously).

    For 'fast' tier, uses solve_with_callbacks if available.
    For 'instant' tier, uses plain solve.
    """
    if tier == "fast":
        try:
            from sage_solver_core.solver import solve_with_callbacks
            return solve_with_callbacks(si)
        except Exception:
            # Fall back to plain solve
            return solve(si)
    return solve(si)


def _register_completed_job(
    result: SolverResult,
    model: AnyModel,
    si: SolverInput,
    problem_name: str | None = None,
) -> str | None:
    """Register a completed solve as a job in sage-solver-cloud.

    Two-step process:
    1. POST /api/jobs — create the job record (status: queued)
    2. PATCH /api/jobs/{task_id}/complete — mark complete with results

    Returns the cloud-assigned task_id, or None if cloud is unavailable.
    """
    if not _state.cloud_url:
        return None

    # Determine problem type string
    model_type = type(model).__name__
    type_map = {
        "LPModel": "LP",
        "MIPModel": "MIP",
        "PortfolioModel": "PORTFOLIO",
        "SchedulingModel": "SCHEDULING",
    }
    problem_type_str = type_map.get(model_type, "LP")

    try:
        # Step 1: Create job record
        create_resp = _cloud_post("/api/jobs", {
            "solver_input": si.model_dump(mode="json"),
            "problem_name": problem_name or getattr(model, "name", "unnamed"),
            "problem_type": problem_type_str,
        })
        if not create_resp or "task_id" not in create_resp:
            logger.debug("Failed to create job record in cloud")
            return None

        task_id = create_resp["task_id"]

        # Step 2: Mark complete with results
        _cloud_patch(f"/api/jobs/{task_id}/complete", {
            "solver_result": result.model_dump(mode="json"),
            "elapsed_seconds": result.solve_time_seconds or 0.0,
            "explanation": None,
        })

        return task_id
    except Exception:
        logger.debug("Failed to register completed job in cloud", exc_info=True)
        return None


def _submit_to_cloud(
    si: SolverInput, model: AnyModel, problem_name: str
) -> dict | None:
    """Submit a background job to sage-solver-cloud.

    Returns the cloud response dict with task_id, or None on failure.
    """
    if not _state.cloud_url:
        return None
    type_map = {
        "LPModel": "LP",
        "MIPModel": "MIP",
        "PortfolioModel": "PORTFOLIO",
        "SchedulingModel": "SCHEDULING",
    }
    try:
        body = {
            "solver_input": si.model_dump(mode="json"),
            "problem_name": problem_name,
            "problem_type": type_map.get(type(model).__name__, "MIP"),
        }
        return _cloud_post("/api/jobs", body)
    except Exception:
        logger.debug("Failed to submit job to cloud", exc_info=True)
        return None


